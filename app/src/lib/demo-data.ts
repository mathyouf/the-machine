// Helper: get videos by primary dimension
export function getVideosByDimension(dim: RentfrowDimension): Video[] {
  const key = `dim_${dim}` as keyof Video;
  return DEMO_VIDEOS.filter((v) => (v[key] as number) >= 0.6).sort(
    (a, b) => (b[key] as number) - (a[key] as number)
  );
}

// System default queue order (Section 6)
export function getSystemDefaultQueue(): Video[] {
  const dims: RentfrowDimension[] = [
    "cerebral",
    "thrilling",
    "aesthetic",
    "communal",
    "dark",
    "cerebral",
    "aesthetic",
    "thrilling",
    "communal",
    "dark",
  ];
  return dims.map((dim) => {
    const pool = getVideosByDimension(dim);
    return pool[Math.floor(Math.random() * pool.length)];
  });
}

